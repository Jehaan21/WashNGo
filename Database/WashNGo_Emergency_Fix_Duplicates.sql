-- Step 1: Check what we have right now
SELECT
    DayOfWeek,
    COUNT(*) AS TotalSlots,
    SUM(CAST(IsActive AS INT)) AS ActiveSlots
FROM TimeSlots
GROUP BY DayOfWeek
ORDER BY DayOfWeek;
GO

-- Step 2: Disable FK constraint so we can delete freely
ALTER TABLE Bookings NOCHECK CONSTRAINT FK_Bookings_Slots;
PRINT 'FK constraint disabled.';

-- Step 3: Delete ALL slots (clean slate)
DELETE FROM TimeSlots;
PRINT 'All slots deleted. Count: ' + CAST(@@ROWCOUNT AS VARCHAR);

-- Step 4: Re-enable FK constraint
ALTER TABLE Bookings CHECK CONSTRAINT FK_Bookings_Slots;
PRINT 'FK constraint re-enabled.';
GO

