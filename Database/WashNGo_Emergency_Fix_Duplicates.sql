        WHEN 6 THEN 'Saturday'
    END AS [Day],
    COUNT(*)                          AS [Total],
    SUM(CAST(IsActive AS INT))        AS [Active],
    COUNT(*)-SUM(CAST(IsActive AS INT)) AS [Inactive]
FROM TimeSlots
GROUP BY DayOfWeek
ORDER BY DayOfWeek;
